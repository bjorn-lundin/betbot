select count('a'), B.BETNAME, R.STATUS
from ABETS B, ALL_RUNNERS R
where B.MARKETID=R.MARKETID
and B.SELECTIONID = R.SELECTIONID
and B.BETNAME in (
  'HORSES_WIN_BACK_FINISH_1.05_7.0_1',
  'HORSES_PLC_BACK_FINISH_1.05_7.0_1',
  'HORSES_WIN_BACK_FINISH_1.10_7.0_1',
  'HORSES_PLC_BACK_FINISH_1.10_7.0_1',
  'HORSES_WIN_BACK_FINISH_1.05_10.0_1',
  'HORSES_PLC_BACK_FINISH_1.05_10.0_1',
  'HORSES_WIN_BACK_FINISH_1.10_10.0_1',
  'HORSES_PLC_BACK_FINISH_1.10_10.0_1',
  'HORSES_PLC_BACK_FINISH_1.50_30.0_1',
  'HORSES_WIN_BACK_FINISH_1.50_30.0_2',
  'HORSES_WIN_BACK_FINISH_1.50_20.0_2',
  'HORSES_WIN_BACK_FINISH_1.50_10.0_2',
  'HORSES_WIN_BACK_FINISH_1.30_30.0_2',
  'HORSES_WIN_BACK_FINISH_1.30_20.0_2',
  'HORSES_WIN_BACK_FINISH_1.30_10.0_2'
)
and R.STATUS in ('WINNER','LOSER')
group by B.BETNAME, R.STATUS
order by B.BETNAME, R.STATUS